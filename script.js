function passavalor() {
  
    if(document.getElementById("check1").checked) {
      let nomeinfo = document.getElementById("check1").value;
      localStorage.setItem("textovalue", nomeinfo);
      
      return false
    }
    
  
    else {
       localStorage.setItem("textovalue", "não.");
  
    }
  
  }
  
  
